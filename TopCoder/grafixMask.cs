        public int[] sortedAreas(string[] rectangles)
        {
            bool[,] marked = new bool[600, 400];

            foreach (string rectangle in rectangles)
            {
                string[] tokens = rectangle.Split(' ');
                int top = int.Parse(tokens[0]);
                int left = int.Parse(tokens[1]);
                int bottom = int.Parse(tokens[2]);
                int right = int.Parse(tokens[3]);

                for (int i = left; i <= right; i++)
                {
                    for (int j = top; j <= bottom; j++)
                        marked[i, j] = true;
                }
            }

            List<int> areas = new List<int>();

            for (int i = 0; i < marked.GetLength(0); i++)
            {
                for (int j = 0; j < marked.GetLength(1); j++)
                {
                    if (marked[i, j])
                        continue;

                    Queue<Point> queue = new Queue<Point>();
                    marked[i, j] = true;
                    queue.Enqueue(new Point { X = i, Y = j });
                    int area = 0;

                    while (queue.Count > 0)
                    {
                        Point current = queue.Dequeue();
                        area++;

                        foreach (Point delta in grafixMask.Deltas)
                        {
                            int x = current.X + delta.X;
                            int y = current.Y + delta.Y;

                            if (x < 0 || x >= marked.GetLength(0) || y < 0 || y >= marked.GetLength(1))
                                continue;

                            if (marked[x, y])
                                continue;

                            marked[x, y] = true;
                            queue.Enqueue(new Point { X = x, Y = y });
                        }
                    }

                    areas.Add(area);
                }
            }

            areas.Sort();
            return areas.ToArray();
        }

        private class Point
        {
            public int X { get; set; }
            public int Y { get; set; }
        }

        private static Point[] Deltas = new Point[]
        {
            new Point { X = 1, Y = 0 },
            new Point { X = 0, Y = 1 },
            new Point { X = -1, Y = 0 },
            new Point { X = 0, Y = -1 },
        };
    }

